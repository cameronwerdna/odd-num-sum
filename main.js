//clever solution if i do say so myself. function takes in a row number and returns the sum of all odd numbers that would be in that row.
// (row 1 contains number 1, row 2 contains numbers 3 & 5, row 3 contains numbers 7, 9, & 11 etc) 
function rowSumOddNumbers(n) {
    return Math.pow(n, 3);
  }


console.log(rowSumOddNumbers(3)) // => 27
console.log(rowSumOddNumbers(9)) // => 729
console.log(rowSumOddNumbers(21)) // => 9261